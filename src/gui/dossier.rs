/// Render `body` inside a framed card with a staggered fade-and-rise reveal.
fn anim_card(ui: &mut egui::Ui, idx: usize, now: f64, anim_start: f64,
             body: impl FnOnce(&mut egui::Ui)) {
    let dur = 0.5;
    let delay = idx as f64 * 0.09;
    let t = (((now - anim_start - delay) / dur).clamp(0.0, 1.0)) as f32;
    let e = ease_out_cubic(t);
    ui.add_space(8.0 + (1.0 - e) * 14.0);
    ui.scope(|ui| {
        ui.set_opacity(e.max(0.02));
        egui::Frame::none()
            .fill(bg_panel())
            .stroke(Stroke::new(1.0, border()))
            .rounding(Rounding::same(12.0))
            .inner_margin(Margin::symmetric(18.0, 14.0))
            .show(ui, |ui| {
                ui.set_width(ui.available_width());
                body(ui);
            });
    });
}

