/// Percent-encode a string for a URL query value (spaces → %20).
fn qenc(s: &str) -> String {
    let mut o = String::new();
    for b in s.bytes() {
        match b {
            b'A'..=b'Z' | b'a'..=b'z' | b'0'..=b'9' | b'-' | b'_' | b'.' | b'~' => o.push(b as char),
            _ => o.push_str(&format!("%{b:02X}")),
        }
    }
    o
}

/// Top-N candidate article titles for a query in one language.
async fn wiki_candidates(client: &reqwest::Client, lang: &str, query: &str, limit: u32) -> Vec<String> {
    let url = format!("https://{lang}.wikipedia.org/w/api.php?action=query&list=search&srsearch={}&srlimit={limit}&format=json&origin=*", qenc(query));
    let j: Value = match client.get(&url).send().await {
        Ok(r) => match r.json().await { Ok(j) => j, Err(_) => return Vec::new() },
        Err(_) => return Vec::new(),
    };
    j["query"]["search"].as_array().map(|a| a.iter()
        .filter_map(|x| x["title"].as_str().map(|s| s.to_string())).collect()).unwrap_or_default()
}

/// Crude relevance score of a candidate title to the user's query (case- and
/// order-insensitive token matching, with bonuses for prefix/exact matches).
fn title_score(query: &str, title: &str) -> i32 {
    let q = query.trim().to_lowercase();
    let t = title.to_lowercase();
    if t == q { return 1000; }
    let mut s = 0;
    if t.starts_with(&q) || q.starts_with(&t) { s += 450; }
    let qt: Vec<&str> = q.split_whitespace().filter(|w| w.len() > 1).collect();
    let mut hit = 0;
    for w in &qt { if t.contains(w) { s += 130; hit += 1; } }
    if !qt.is_empty() && hit == qt.len() { s += 200; } // all query words present
    s -= (t.chars().count() as i32 - q.chars().count() as i32).abs() / 3;
    s
}

/// Resolve a free-text query to the best Wikipedia (lang, title). Searches both
/// the app language and English (script-aware order) and ranks candidates, so
/// e.g. "linus torvald" → ("en","Linus Torvalds") even with a Russian UI.
async fn wiki_resolve(client: &reqwest::Client, app: &str, query: &str) -> Option<(String, String)> {
    let has_cyrillic = query.chars().any(|c| ('\u{0400}'..='\u{04FF}').contains(&c));
    let langs: [&str; 2] = if has_cyrillic { [app, "en"] } else { ["en", app] };
    let mut best: Option<(String, String, i32)> = None;
    let mut seen: Vec<&str> = Vec::new();
    for l in langs {
        if seen.contains(&l) { continue; }
        seen.push(l);
        for t in wiki_candidates(client, l, query, 5).await {
            let sc = title_score(query, &t);
            if best.as_ref().map(|b| sc > b.2).unwrap_or(true) {
                best = Some((l.to_string(), t, sc));
            }
        }
    }
    best.map(|(l, t, _)| (l, t))
}

/// Percent-encode a Wikipedia title for a path segment (spaces → underscore).
fn enc(s: &str) -> String {
    let mut o = String::new();
    for b in s.bytes() {
        match b {
            b'A'..=b'Z' | b'a'..=b'z' | b'0'..=b'9' | b'-' | b'_' | b'.' | b'~' => o.push(b as char),
            b' ' => o.push('_'),
            _ => o.push_str(&format!("%{b:02X}")),
        }
    }
    o
}

async fn fetch(query: String, custom: Vec<RawRecord>, lang: Lang, tx: Sender<DMsg>) {
    let client = client();
    let mut d = Dossier::empty(&query);
    let mut got = false;
    let mut thumb_url: Option<String> = None;

    // 1 ── Resolve the query to a real article title first (this is the "smart"
    // part: it fixes casing like "linus torvalds", typos and partial names), then
    // fetch the summary. Try the app language, then English.
    // Build an ordered list of (lang, title) attempts: the ranked search hit
    // first, then the raw query in the app language and in English.
    let mut attempts: Vec<(String, String)> = Vec::new();
    if let Some((l, t)) = wiki_resolve(&client, lang.code(), &query).await {
        let _ = tx.send(DMsg::Log(format!("◦  best match: {l}:{t}")));
        attempts.push((l, t));
    }
    attempts.push((lang.code().to_string(), query.clone()));
    if lang.code() != "en" { attempts.push(("en".to_string(), query.clone())); }

    let mut summary: Option<Value> = None;
    for (l, t) in &attempts {
        let url = format!("https://{l}.wikipedia.org/api/rest_v1/page/summary/{}", enc(t));
        match client.get(&url).send().await {
            Ok(r) if r.status().is_success() => match r.json::<Value>().await {
                Ok(j) if j["type"].as_str() == Some("disambiguation") => {
                    let _ = tx.send(DMsg::Log(format!("⚠  “{t}” is a disambiguation page — trying next")));
                }
                Ok(j) => { summary = Some(j); break; }
                Err(_) => {}
            },
            Ok(_) => {}
            Err(e) => { let _ = tx.send(DMsg::Log(format!("✗  Wikipedia error: {e}"))); }
        }
    }
    match summary {
        Some(j) => {
            got = true;
            d.title = j["title"].as_str().unwrap_or(&query).to_string();
            if let Some(s) = j["description"].as_str() { d.subtitle = s.to_string(); }
            if let Some(s) = j["extract"].as_str() { d.summary = s.to_string(); }
            d.url = j["content_urls"]["desktop"]["page"].as_str().map(|s| s.to_string());
            thumb_url = j["thumbnail"]["source"].as_str().map(|s| s.to_string());
            d.source_label = "Wikipedia".into();
            if let Some(u) = d.url.clone() { d.links.push(("Wikipedia".into(), u)); }
            let _ = tx.send(DMsg::Log(format!("✓  Wikipedia: {}", d.title)));

            // 2 ── Wikidata structured facts + cross-language sources
            if let Some(qid) = j["wikibase_item"].as_str() {
                let _ = tx.send(DMsg::Log(format!("◦  Wikidata {qid}…")));
                if let Some((mut secs, links, mut more, img)) =
                    fetch_wikidata(&client, qid, lang).await
                {
                    d.sections.append(&mut secs);
                    for l in links { if !d.links.iter().any(|(k, _)| *k == l.0) { d.links.push(l); } }
                    d.more.append(&mut more);
                    if thumb_url.is_none() { thumb_url = img; }
                    d.source_label = "Wikipedia + Wikidata".into();
                    let _ = tx.send(DMsg::Log("✓  Wikidata facts merged".into()));
                }
            }
        }
        None => { let _ = tx.send(DMsg::Log(format!("◦  no Wikipedia page for “{query}”"))); }
    }

    // 3 ── Custom database overlay
    if let Some((name, _, role, summary, facts, links)) = match_custom(&custom, &query) {
        got = true;
        if d.title.is_empty() { d.title = name; }
        if d.subtitle.is_empty() { if let Some(r) = role { d.subtitle = r; } }
        if d.summary.is_empty() { if let Some(s) = summary { d.summary = s; } }
        if !facts.is_empty() {
            d.sections.push(Section { title: translate(lang, "sec.custom").into(), icon: "▦", facts });
        }
        for (k, v) in links { if !d.links.iter().any(|(lk, _)| *lk == k) { d.links.push((k, v)); } }
        d.source_label = if d.source_label.is_empty() {
            "Custom DB".into()
        } else {
            format!("{} + Custom DB", d.source_label)
        };
        let _ = tx.send(DMsg::Log("✓  matched a custom database record".into()));
    }

    if !got {
        let _ = tx.send(DMsg::Failed(format!("nothing found for “{query}”")));
        return;
    }

    // discovery links — always available, so the report points further out
    let qs = query.trim().replace(' ', "+");
    d.more.push((translate(lang, "l.web_search").to_string(),
                 format!("https://duckduckgo.com/?q={qs}")));
    d.more.push(("Google".into(), format!("https://www.google.com/search?q={qs}")));

    d.thumb_url = thumb_url.clone();
    let _ = tx.send(DMsg::Done(Box::new(d)));

    // 4 ── thumbnail (fetched after the card is shown so it pops in)
    if let Some(tu) = thumb_url {
        if let Ok(resp) = client.get(&tu).send().await {
            if let Ok(bytes) = resp.bytes().await {
                let _ = tx.send(DMsg::Thumb(bytes.to_vec()));
            }
        }
    }
}

/// Find the first custom record whose name / aka matches the query.
fn match_custom(custom: &[RawRecord], query: &str) -> Option<RawRecord> {
    let q = query.trim().to_lowercase();
    custom.iter().find(|(name, aka, ..)| {
        name.to_lowercase() == q
            || name.to_lowercase().contains(&q)
            || aka.iter().any(|a| a.to_lowercase() == q)
    }).cloned()
}

/// Pull a curated, human-readable set of facts from a Wikidata entity.
/// Labels are resolved in the app language (falling back to English), and the
/// section/fact captions are translated. Returns (sections, links, more, image).
async fn fetch_wikidata(client: &reqwest::Client, qid: &str, lang: Lang)
    -> Option<(Vec<Section>, Vec<(String, String)>, Vec<(String, String)>, Option<String>)>
{
    let url = format!("https://www.wikidata.org/wiki/Special:EntityData/{qid}.json");
    let j: Value = client.get(&url).send().await.ok()?.json().await.ok()?;
    let entity = j["entities"][qid].clone();
    let claims = entity["claims"].clone();
    if !claims.is_object() { return None; }
    let tl = |k: &str| translate(lang, k).to_string();

    // collect entity-referencing property values so their labels can be resolved
    let ref_props = ["P21", "P19", "P20", "P27", "P106", "P108", "P69",
                     "P166", "P39", "P26", "P140", "P101"];
    let mut want: HashSet<String> = HashSet::new();
    for p in ref_props { for q in entity_ids(&claims, p) { want.insert(q); } }
    let labels = resolve_labels(client, &want, lang.code()).await;

    let lbl = |q: &str| labels.get(q).cloned().unwrap_or_else(|| q.to_string());
    let first = |p: &str| entity_ids(&claims, p).first().map(|q| lbl(q));
    let join = |p: &str, n: usize| {
        let v: Vec<String> = entity_ids(&claims, p).iter().take(n).map(|q| lbl(q)).collect();
        if v.is_empty() { None } else { Some(v.join(", ")) }
    };

    // Identity
    let mut identity = Vec::new();
    if let Some(g) = first("P21")            { identity.push((tl("f.gender"), g)); }
    if let Some(t) = time(&claims, "P569")   { identity.push((tl("f.born"), t)); }
    if let Some(b) = first("P19")            { identity.push((tl("f.birthplace"), b)); }
    if let Some(c) = join("P27", 3)          { identity.push((tl("f.citizenship"), c)); }
    if let Some(t) = time(&claims, "P570")   { identity.push((tl("f.died"), t)); }
    if let Some(b) = first("P20")            { identity.push((tl("f.death_place"), b)); }

    // Career
    let mut career = Vec::new();
    if let Some(o) = join("P106", 5) { career.push((tl("f.occupation"), o)); }
    if let Some(e) = join("P108", 3) { career.push((tl("f.employer"), e)); }
    if let Some(e) = join("P69", 3)  { career.push((tl("f.education"), e)); }
    if let Some(p) = join("P39", 3)  { career.push((tl("f.position"), p)); }
    if let Some(a) = join("P166", 5) { career.push((tl("f.awards"), a)); }

    // Personal
    let mut personal = Vec::new();
    if let Some(s) = join("P26", 3) { personal.push((tl("f.spouse"), s)); }
    if let Some(f) = first("P101")  { personal.push((tl("f.field"), f)); }
    if let Some(r) = first("P140")  { personal.push((tl("f.religion"), r)); }

    let mut sections = Vec::new();
    if !identity.is_empty() { sections.push(Section { title: tl("sec.identity"), icon: "◉", facts: identity }); }
    if !career.is_empty()   { sections.push(Section { title: tl("sec.career"),   icon: "▤", facts: career }); }
    if !personal.is_empty() { sections.push(Section { title: tl("sec.personal"), icon: "❤", facts: personal }); }

    // Online presence (string / external-id / url properties)
    let mut links = Vec::new();
    if let Some(u) = strings(&claims, "P856").into_iter().next()  { links.push((tl("l.official"), u)); }
    if let Some(u) = strings(&claims, "P2002").into_iter().next() { links.push(("Twitter / X".into(), format!("https://twitter.com/{u}"))); }
    if let Some(u) = strings(&claims, "P2037").into_iter().next() { links.push(("GitHub".into(), format!("https://github.com/{u}"))); }
    if let Some(u) = strings(&claims, "P2003").into_iter().next() { links.push(("Instagram".into(), format!("https://instagram.com/{u}"))); }
    if let Some(u) = strings(&claims, "P2013").into_iter().next() { links.push(("Facebook".into(), format!("https://facebook.com/{u}"))); }
    if let Some(u) = strings(&claims, "P345").into_iter().next()  { links.push(("IMDb".into(), format!("https://www.imdb.com/name/{u}"))); }

    // More sources — the Wikidata item itself + the article in each language
    let mut more = Vec::new();
    more.push(("Wikidata".into(), format!("https://www.wikidata.org/wiki/{qid}")));
    for (site, label) in [("enwiki", "Wikipedia (EN)"), ("ruwiki", "Wikipedia (RU)"),
                          ("ukwiki", "Wikipedia (UK)")] {
        if let Some(title) = entity["sitelinks"][site]["title"].as_str() {
            let sub = site.trim_end_matches("wiki");
            more.push((label.to_string(),
                format!("https://{sub}.wikipedia.org/wiki/{}", enc(title))));
        }
    }

    // photo (Commons image) as a thumbnail fallback
    let img = strings(&claims, "P18").into_iter().next()
        .map(|f| format!("https://commons.wikimedia.org/wiki/Special:FilePath/{}?width=400", enc(&f)));

    Some((sections, links, more, img))
}

fn entity_ids(claims: &Value, pid: &str) -> Vec<String> {
    claims[pid].as_array().map(|arr| arr.iter().filter_map(|c|
        c["mainsnak"]["datavalue"]["value"]["id"].as_str().map(|s| s.to_string())
    ).collect()).unwrap_or_default()
}

fn strings(claims: &Value, pid: &str) -> Vec<String> {
    claims[pid].as_array().map(|arr| arr.iter().filter_map(|c|
        c["mainsnak"]["datavalue"]["value"].as_str().map(|s| s.to_string())
    ).collect()).unwrap_or_default()
}

fn time(claims: &Value, pid: &str) -> Option<String> {
    let v = &claims[pid].get(0)?["mainsnak"]["datavalue"]["value"];
    let t = v["time"].as_str()?;
    let prec = v["precision"].as_u64().unwrap_or(11);
    Some(fmt_wd_time(t, prec))
}

/// Format a Wikidata time literal ("+1969-12-28T00:00:00Z") by precision.
fn fmt_wd_time(t: &str, prec: u64) -> String {
    let s = t.strip_prefix('+').unwrap_or(t);
    let date = s.split('T').next().unwrap_or(s);
    let parts: Vec<&str> = date.split('-').collect();
    match prec {
        p if p <= 9 => parts.first().copied().unwrap_or(date).to_string(),
        10          => parts.iter().take(2).copied().collect::<Vec<_>>().join("-"),
        _           => date.to_string(),
    }
}

/// Batch-resolve Wikidata Q-ids → labels in `lang` (with an English fallback).
async fn resolve_labels(client: &reqwest::Client, want: &HashSet<String>, lang: &str)
    -> HashMap<String, String>
{
    let mut out = HashMap::new();
    if want.is_empty() { return out; }
    let ids: Vec<&String> = want.iter().collect();
    for chunk in ids.chunks(50) {
        let joined = chunk.iter().map(|s| s.as_str()).collect::<Vec<_>>().join("|");
        let url = format!("https://www.wikidata.org/w/api.php?action=wbgetentities\
                           &ids={joined}&props=labels&languages={lang}|en&format=json");
        if let Ok(resp) = client.get(&url).send().await {
            if let Ok(j) = resp.json::<Value>().await {
                if let Some(ents) = j["entities"].as_object() {
                    for (q, ev) in ents {
                        let label = ev["labels"][lang]["value"].as_str()
                            .or_else(|| ev["labels"]["en"]["value"].as_str());
                        if let Some(l) = label { out.insert(q.clone(), l.to_string()); }
                    }
                }
            }
        }
    }
    out
}
