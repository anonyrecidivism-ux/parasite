//! Dossier mode — parasite's "understands people" workspace.
//!
//! Type a subject (a person, organization or place) and parasite assembles a
//! readable profile from a stack of sources:
//!   • Wikipedia REST summary  → title, one-line description, lead paragraph, photo
//!   • Wikidata claims         → structured biography (born/died, citizenship,
//!                                occupation, education, employer, awards, socials)
//!   • a user-supplied JSON DB  → your own records, merged on top
//!
//! The result is rendered as an animated, card-based report and can be exported
//! to Markdown, JSON, PNG and PDF (the latter two reuse the graph's screenshot
//! pipeline). This is an experimental feature.

