// ── Data model ──────────────────────────────────────────────────────────────

#[derive(Serialize, Clone)]
pub struct Section {
    pub title: String,
    pub icon:  &'static str,
    pub facts: Vec<(String, String)>,
}

#[derive(Serialize, Clone, Default)]
pub struct Dossier {
    pub title:        String,
    pub subtitle:     String,
    pub summary:      String,
    pub sections:     Vec<Section>,
    pub links:        Vec<(String, String)>,
    pub more:         Vec<(String, String)>,
    pub source_label: String,
    pub url:          Option<String>,
    pub thumb_url:    Option<String>,
}

impl Dossier {
    fn empty(query: &str) -> Self {
        Self { title: query.to_string(), ..Default::default() }
    }

    fn to_markdown(&self) -> String {
        let mut s = String::new();
        s.push_str(&format!("# {}\n\n", self.title));
        if !self.subtitle.is_empty() { s.push_str(&format!("*{}*\n\n", self.subtitle)); }
        if !self.source_label.is_empty() {
            s.push_str(&format!("> Sources: {}\n\n", self.source_label));
        }
        if !self.summary.is_empty() { s.push_str(&format!("{}\n\n", self.summary)); }
        for sec in &self.sections {
            s.push_str(&format!("## {} {}\n\n", sec.icon, sec.title));
            for (k, v) in &sec.facts { s.push_str(&format!("- **{k}:** {v}\n")); }
            s.push('\n');
        }
        if !self.links.is_empty() {
            s.push_str("## ❖ Links\n\n");
            for (k, v) in &self.links { s.push_str(&format!("- **{k}:** {v}\n")); }
            s.push('\n');
        }
        if !self.more.is_empty() {
            s.push_str("## ⊕ More sources\n\n");
            for (k, v) in &self.more { s.push_str(&format!("- **{k}:** {v}\n")); }
            s.push('\n');
        }
        s.push_str("---\n_Generated by parasite · Dossier mode_\n");
        s
    }
}

/// A record in a user-supplied JSON database.
#[derive(Deserialize, Default)]
struct CustomRecord {
    #[serde(default)] name:    String,
    #[serde(default)] aka:     Vec<String>,
    #[serde(default)] role:    Option<String>,
    #[serde(default)] summary: Option<String>,
    #[serde(default)] facts:   BTreeMap<String, String>,
    #[serde(default)] links:   BTreeMap<String, String>,
}

#[derive(Deserialize)]
struct CustomWrapper { #[serde(default)] records: Vec<CustomRecord> }

